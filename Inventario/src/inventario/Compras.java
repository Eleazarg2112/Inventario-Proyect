    private void jLabel1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel1MouseClicked
     cantidad = JOptionPane.showInputDialog(null, "Ingrese la cantidad a comprar: ");
      ncantidad = Integer.parseInt(cantidad);
      code = jTextField1.getText();
    for(Product pro : product)
    {  
        if(pro.getCode().equals(code))
        {   
            pro.setAmount(pro.getAmount() + ncantidad);
           int y = pro.getAmount();
           
           String values = String.format("Cantidad=\'%s\'", String.valueOf(y));
            String sql = "update medicamento set "+ values +  " where Codigo='" + code + "'";
        try {
            PreparedStatement pst = cn.prepareStatement(sql);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null,"Compra exitosa");
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
        }
        }
    
    } 
    }//GEN-LAST:event_jLabel1MouseClicked

