    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int n = 0;
        
        try {
            PreparedStatement pat = cn.prepareStatement("INSERT INTO medicamento(Codigo,Nombre,Marca,Descripcion,Expiracion,Costo,Precio,Cantidad) VALUES(?,?,?,?,?,?,?,?)");
            pat.setString(1, jTextField9.getText());
            pat.setString(2, jTextField10.getText());
            pat.setString(3, jTextField11.getText());
            pat.setString(4, jTextField12.getText());
            pat.setString(5, jTextField1.getText());
            pat.setDouble(6, Double.parseDouble(jTextField14.getText()));
            pat.setDouble(7, Double.parseDouble(jTextField15.getText()));
            pat.setDouble(8, Double.parseDouble(jTextField16.getText()));
           n =  pat.executeUpdate();
        } catch (SQLException ex) {
            Logger.getLogger(Compras.class.getName()).log(Level.SEVERE, null, ex);
        }
         if(n > 0)
         {  JOptionPane.showMessageDialog(null, "Agregado!");
             jTextField9.setText("");
             jTextField10.setText("");
             jTextField11.setText("");
             jTextField12.setText("");
             jTextField1.setText("");
             jTextField14.setText("");
             jTextField15.setText("");
             jTextField16.setText("");

         }else
             JOptionPane.showMessageDialog(null, "Error al agregar el medicamente", "", JOptionPane.ERROR_MESSAGE);
    }//GEN-LAST:event_jButton3ActionPerformed

